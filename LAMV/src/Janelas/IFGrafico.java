    private void TDadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TDadosMouseClicked
// TODO add your handling code here:
        int row = TDados.getSelectedRow();
        boolean b = false;
        if( row >= 0 ) {
            b = true;
        } else {
            COMPONENTE = null;
            b = false;
        }

        CBLinhas.setEnabled(b);
        CBPontos.setEnabled(b);
        TBQuadrado.setEnabled(b);
        TBCirculo.setEnabled(b);
        PCorGraf.setEnabled(b);
        
        if( !b ) return;

        COMPONENTE = (String)TDados.getValueAt(row,0);
        PCorGraf.setBackground( GG.getCorComponente(COMPONENTE) );
        CBLinhas.setSelected( GG.verRetasComponente(COMPONENTE) );
        CBPontos.setSelected( GG.verPontosComponente(COMPONENTE) );
        TBQuadrado.setSelected( GG.getTipoPontoComponete(COMPONENTE) == Dado.RECTF ? true : false );
        TBCirculo.setSelected( GG.getTipoPontoComponete(COMPONENTE) == Dado.CIRCLEF ? true : false );
        GG.setCorComponente( COMPONENTE, PCorGraf.getBackground() );
    }//GEN-LAST:event_TDadosMouseClicked
    
