    private void BSalvarGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSalvarGActionPerformed
        // TODO add your handling code here:
        JFileChooser f = new JFileChooser();
        //this.lista = lista;
        String extensao;
        JFrame J = new JFrame();
        f.setFileFilter(new FileFilter() //Filtro de exibição para extensão definida
        {
            public boolean accept(File f) {
                return f.getName().toLowerCase().endsWith(".eps")||
                        f.getName().toLowerCase().endsWith(".jpeg") || f.isDirectory();
            }
            public String getDescription() {
                return ".jpeg ou .eps";
            }
        });
        if(f.APPROVE_OPTION == f.showSaveDialog(J)) {
            File arquivo = f.getSelectedFile();
            
            FileFilter Filtro = f.getFileFilter();
            extensao = Filtro.getDescription();
            if ( arquivo == null || arquivo.getName().equals("") || arquivo.getName().trim().equals("") ) {
                JOptionPane.showMessageDialog(new JFrame(), "Nome de arquivo inválido",
                        "Nome de arquivo inválido", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String caminho_config = new String(f.getSelectedFile().getPath());
            String caminho = new String(f.getSelectedFile().getPath());
            StringTokenizer str = new StringTokenizer(caminho,".");
            if(str.countTokens() == 1)//usuário não digitou extensão - jpeg é default
            {
                extensao = ".jpeg";
            } else {
                extensao = str.nextToken(); //pula o nome
                extensao = "."+str.nextToken();//pega a extensao
            }
            if( extensao.equals(".jpeg")) {
                leDisco disco = new leDisco();
                disco.salvaJPEG(this.getContentPane(), GG, caminho+extensao);
            } else if (extensao.equals(".eps")) {
                java.awt.Container cp = this.getContentPane();
                final java.awt.Component comp = cp.add(GG);
                javax.swing.JPanel south = new javax.swing.JPanel();
                try {
                    int w = comp.getWidth(), h = comp.getHeight();
                    java.awt.image.BufferedImage image = new java.awt.image.BufferedImage(w, h, java.awt.image.BufferedImage.TYPE_INT_RGB);
                    Componentes.Grafico.EpsGraphics2D g2 = new Componentes.Grafico.EpsGraphics2D(caminho, new FileOutputStream(caminho+extensao), 0, 0, w, h);
                    comp.paint(g2);
                    g2.dispose();
                } catch (IOException e) {
                    System.err.println(e);
                }
            } else {
                Sistema.FSys.Msg("Erro", "Esta versão suporta apenas imagens de extensão .jpeg e eps",0);
            }
        }
    }//GEN-LAST:event_BSalvarGActionPerformed
    
